import { PrismaClient } from '@prisma/client';
const prisma = new PrismaClient();

async function main() {
    console.log('--- GENERATING EXPERT 3-SENTENCE DESCRIPTIONS ---');

    const products = await prisma.product.findMany({
        include: { brand: true }
    });

    for (const p of products) {
        const nameL = p.name.toLowerCase();
        const brand = p.brand.name;
        let newDesc = "";

        // 1. ТРАПЫ (Боковой vs Прямой)
        if (nameL.includes('трап') || nameL.includes('желоб')) {
            if (nameL.includes('боков')) {
                newDesc = `Боковой слив ${brand} — это компактное инженерное решение для квартир, позволяющее минимизировать высоту стяжки пола в душевой зоне. Конструкция адаптирована под горизонтальное подключение к канализации в условиях ограниченного межэтажного пространства. Технологически верный выбор для сохранения эстетики низкого порога без потери пропускной способности.`;
            } else if (nameL.includes('вертикал') || nameL.includes('прям')) {
                newDesc = `Прямой (вертикальный) слив ${brand} обеспечивает максимальную пропускную способность и эффект самоочистки за счет силы гравитации при отводе воды напрямую в перекрытие. Это решение идеально подходит для частных домов или первых этажей, где есть доступ к коммуникациям снизу. Главное преимущество системы заключается в мгновенном отводе сверхбольших объемов воды без риска перелива.`;
            } else {
                newDesc = `Дренажная система от ${brand} представляет собой надежный узел водоотведения с оптимизированной конфигурацией корпуса для быстрой установки. Технология гидрозатвора гарантирует надежную защиту от канализационных запахов даже при редком использовании душа. Конструкция рассчитана на длительную эксплуатацию в агрессивной влажной среде.`;
            }
        }
        // 2. СМЕСИТЕЛИ (SilkMove, PVD, Ceramic)
        else if (nameL.includes('смеситель')) {
            newDesc = `Смеситель ${brand} оснащен высокоточным керамическим картриджем, обеспечивающим плавную регулировку температуры и напора на протяжении всего срока службы. Внешнее покрытие выполнено по технологии, повышающей твердость поверхности и устойчивость к механическим повреждениям. Внутренняя архитектура водных каналов оптимизирована для снижения шума и экономии расхода воды.`;
        }
        // 3. ИНСТАЛЛЯЦИИ И КНОПКИ
        else if (nameL.includes('инсталляция') || nameL.includes('кнопка')) {
            newDesc = `Данное инженерное решение от ${brand} разработано для скрытого монтажа и выдерживает статическую нагрузку до 400 кг. Механизмы смыва откалиброваны для безупречного срабатывания и экономичного расхода воды в двух режимах. Полная совместимость компонентов гарантирует отсутствие протечек и простоту обслуживания через ревизионное окно за клавишей.`;
        }
        // 4. УНИТАЗЫ (Rimless, Ceramics)
        else if (nameL.includes('унитаз')) {
            newDesc = `Изделие ${brand} выполнено из санитарного фарфора с низким коэффициентом водопоглощения, что предотвращает впитывание запахов и налета. Технология безободкового смыва (Rimless) обеспечивает идеальную гигиену чаши за счет мощного направленного потока без «слепых зон». Гладкая глазуровка поверхности минимизирует скопление бактерий и облегчает повседневный уход.`;
        }
        // 5. КОМПЛЕКТУЮЩИЕ (Сиденья, Манжеты)
        else if (nameL.includes('сиденье') || nameL.includes('манжет') || nameL.includes('креплен')) {
            newDesc = `Профессиональный комплектующий элемент от ${brand}, выполненный из материалов, устойчивых к химическим и термическим воздействиям. Геометрия изделия точно соответствует техническим стандартам, обеспечивая абсолютную герметичность узла. Надежная фиксация и долговечность материала исключают необходимость частого сервисного обслуживания.`;
        }
        // DEFAULT
        else {
            newDesc = `Продукт ${brand} — это стандарт надежности в сегменте торговой сантехники, выполненный из сертифицированных материалов. Инженерная проработка каждой детали гарантирует стабильную работу изделия в условиях интенсивной эксплуатации. Техническое решение адаптировано под международные стандарты монтажа.`;
        }

        await prisma.product.update({
            where: { id: p.id },
            data: { description: newDesc }
        });
    }

    console.log('--- SUCCESS: ALL PRODUCTS CONVERTED TO GENIUS TONE ---');
}

main().finally(() => prisma.$disconnect());
